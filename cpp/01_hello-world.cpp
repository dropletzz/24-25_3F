#include <iostream>

/*
 * La funzione main viene eseguita in automatico quando parte il programma
 * (si chiama anche "entrypoint", tradotto: "punto di ingresso").
 * Restituisce un numero intero:
 * - 0 se il programma termina senza errori in esecuzione.
 * - un numero diverso da 0 se ci sono stati errori.
 */
int main() {
    std::cout << "Hello, 3F!\n";

    // esco dal main senza errori
    return 0;
}

